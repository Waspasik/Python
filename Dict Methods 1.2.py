# –ø—Ä–æ–±–µ–ª–∞.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É, –Ω–µ —Å–æ–¥–µ—Ä–∂–∞—â—É—é –¥—É–±–ª–∏–∫–∞—Ç–æ–≤ —Å–æ—Ö—Ä–∞–Ω–∏–≤ –ø—Ä–∏ —ç—Ç–æ–º –∏—Å—Ö–æ–¥–Ω—ã–π
# –ø–æ—Ä—è–¥–æ–∫.

lst = [i for i in input().split()]

result = {}
total_list = []

for char in lst:
    if char not in result:
        total_list.append(char)
    else:
        counter_char = [char, '_', str((result.get(char, 0)))]
        total_list.append(''.join(counter_char))
    result[char] = result.get(char, 0) + 1

print(*total_list)