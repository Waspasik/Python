# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –¢–µ–∫—Å—Ç –∏—Å—Ö–æ–¥–Ω–æ–π –ø–µ—Å–Ω–∏ –≤ –ø–µ—Ä–≤–æ–º —Ç–µ—Å—Ç–µ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ü–æ–µ–º –∏ —Ä–µ—à–∞–µ–º, –¥—Ä—É–∑—å—è, –ø–æ–µ–º –∏ —Ä–µ—à–∞–µ–º üòÇ.

word = input() + ' –∑–∞–ø—Ä–µ—Ç–∏–ª –±—É–∫–≤—É'
rus_alphabet = '–∞–±–≤–≥–¥–µ–∂–∑–∏–π–∫–ª–º–Ω–æ–ø—Ä—Å—Ç—É—Ñ—Ö—Ü—á—à—â—ä—ã—å—ç—é—è'
for i in range(len(rus_alphabet)):
    if rus_alphabet[i] in word:
        print(word, rus_alphabet[i], sep=' ')
        length = len(word)
        for j in range(length):
            if word[j] == rus_alphabet[i]:
                word = word.replace(rus_alphabet[i], '')
                if '  ' in word:
                    word = word.replace('  ', ' ')
                word = word.strip()
                break
