# —Ñ—É–Ω–∫—Ü–∏—é zip_longest(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def zip_longest(*args, fill=None):
    for lst in args:
        while len(lst) != len(max(args, key=len)):
            lst.append(fill)
    return [elements for elements in zip(*args)]



