string = input()

lower_case = ''
upper_case = ''
odd_digits = ''
even_digits = ''

for char in string:
    if char.islower():
        lower_case += char
    elif char.isupper():
        upper_case += char
    elif char.isdigit() and int(char) % 2 != 0:
        odd_digits += char
    elif char.isdigit() and int(char) % 2 == 0:
        even_digits += char

print(''.join(sorted(lower_case) + sorted(upper_case) + sorted(odd_digits) + sorted(even_digits)))