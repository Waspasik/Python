# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —É–∫–∞–∑–∞–Ω–Ω—ã–π –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫.

result = []
temp_list = []
for char in input().split():
    if bool(temp_list) == False:
        temp_list.append(char)
    else:
        if char == temp_list[-1]:
            temp_list.append(char)
        else:
            result.append(temp_list)
            temp_list = [char]
if temp_list:
    result.append(temp_list)
print(result)



