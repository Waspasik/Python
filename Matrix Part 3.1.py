n, m = int(numbers[0]), int(numbers[1])
matrix, temp = [], []

for i in range(n):
    for j in range(m):
        if (i + j + 1) % m != 0:
            temp.append((i + j + 1) % m)
        elif (i + j + 1) % m == 0:
            temp.append(m)
    matrix.append(temp)
