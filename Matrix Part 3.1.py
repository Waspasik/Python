matrix, temp = [], []

for i in range(n):
    for j in range(n):
        if i < j and i < n - 1 - j or i > j and i > n - 1 - j or i + j == n - 1 or i - j == 0:
            temp.append(1)
        else:
            temp.append(0)
    matrix.append(temp)
    temp = []

