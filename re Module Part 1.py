# Beegeek
# Напишите программу, определяющую:

# количество строк, в которых bee встречается в качестве подстроки не менее двух раз
# количество строк, в которых geek встречается в качестве слова хотя бы один раз

# Формат входных данных
# На вход программе произвольное количество строк, каждая из которых содержит набор произвольных
# символов.

# Формат выходных данных
# Программа должна вывести два числа:

# первое — количество строк, в которых bee встречается в качестве подстроки не менее двух раз
# второе — количество строк, в которых geek встречается в качестве слова хотя бы один раз

# каждое на отдельной строке.

# Примечание 1. Словом будем считать любую непрерывную последовательность из одной или нескольких
# символов, соответствующих \w.

# римечание 2. Строка может одновременно удовлетворять обеим условиям.

from re import search
import sys


count_bee, count_geek = 0, 0

for string in sys.stdin:
    if search(r'.*(.*bee.*).*\1', string.strip('\n')):
        count_bee += 1
    if search(r'.*(\bgeek\b).*', string.strip('\n')):
        count_geek += 1

print(count_bee, count_geek, sep='\n')



# Популярность
# В онлайн-школе BEEGEEK мы всегда следим за тем, насколько растет наша популярность. Для
# этого мы собираем публикации из различных соцсетей, которые содержат вхождения строки
# beegeek в нижнем регистре. Мы оцениваем публикацию:

# в 3 балла, если она начинается и заканчивается строкой beegeek
# в 2 балла, если она только начинается или только заканчивается строкой beegeek
# в 1 балл, если она содержит строку beegeek только внутри
# в 0 баллов, если она не содержит строку beegeek

# Напишите программу, которая определяет популярность онлайн-школы BEEGEEK путем суммирования
# баллов всех публикаций.

# Формат входных данных
# На вход программе подается произвольное число строк, каждая из которых представляет
# очередную публикацию.

# Формат выходных данных
# Программа должна определить, во сколько баллов оценивается каждая введенная публикация,
# и вывести сумму всех полученных баллов.

# Примечание 1. Если публикация представляет собой просто строку beegeek, то она оценивается
# в 2 балла.

from re import fullmatch
import sys


patterns = {3: r'^beegeek.*beegeek$', 2: r'^beegeek.*$|^.*beegeek$', 1: r'.*beegeek.*'}
total = 0

for string in sys.stdin:
    for grade, pattern in patterns.items():
        if fullmatch(pattern, string.strip('\n')):
            total += grade
            break

print(total)



