# Телефонные номера
# Вам доступен набор телефонных номеров, имеющих следующие форматы:

# код страны>-<код города>-<номер>
# <код страны> <код города> <номер>

# в котором код страны и код города представлены последовательностями от одной до трех
# цифр включительно, а номер — последовательностью от четырех до десяти цифр включительно.
# Между кодом страны, кодом города используется разделитель, которым служит либо символ
# дефис, либо символ пробел, причем одновременно оба вида разделителя в одном номере
# присутствовать не могут.

# Напишите программу, которая принимает произвольное количество телефонных номеров и для
# каждого выводит отдельно его код страны, код города и номер.

# ормат входных данных
# На вход программе подается произвольное количество телефонных номеров, удовлетворяющих
# приведенным выше шаблонам, каждый на отдельной строке.

# ормат выходных данных
# Программа должна для каждого введенного телефонного номера вывести отдельно его код
# страны, код города и номер в следующем формате:

# Код страны: <код страны>, Код города: <код города>, Номер: <номер>

from re import search
import sys


for number in sys.stdin:
    match = search(r'(\d{1,3})(-| )(\d{1,3})\2(\d{4,10})', number.strip('\n'))
    print(f'Код страны: {match.group(1)}, Код города: {match.group(3)}, Номер: {match.group(4)}')



# Онлайн-школа BEEGEEK
# В онлайн-школе BEEGEEK логин учетной записи определяется следующим образом:

# первым символом является символ нижнего подчеркивания _
# затем следуют одна или более цифр
# после записываются ноль или более латинских букв в произвольном регистре
# логин может иметь на конце необязательный символ нижнего подчеркивания _

# Напишите программу, которая принимает произвольное количество строк и определяет, какие
# из них представляют собой корректный логин онлайн-школы BEEGEEK.

# ормат входных данных
# На вход программе подаётся произвольное количество строк, каждая из которых содержит
# набор произвольных символов.

# Формат выходных данных
# Программа должна для каждой введенной строки вывести True, если она представляет собой
# корректный логин онлайн-школы BEEGEEK, или False в противном случае.

from re import fullmatch
import sys


for password in sys.stdin:
    print(True if fullmatch(r'_\d+[A-Za-z]*_?', password.strip('\n')) else False)



