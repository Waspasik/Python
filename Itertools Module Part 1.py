# roundrobin(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

from itertools import zip_longest


def roundrobin(*args):
    wrapper_args = [[item for item in elem] for elem in args]
    zip_args = zip_longest(*wrapper_args, fillvalue='')
    for elem in zip_args:
        for item in elem:
            if item == '':
                continue
            else:
                yield item