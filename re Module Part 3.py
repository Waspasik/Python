# –ü—Ä–æ–≥—Ä–∞–º–º–µ –¥–æ–ª–∂–Ω–∞ —É–¥–∞–ª–∏—Ç—å –≤—Å–µ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ –∏–∑ –≤–≤–µ–¥–µ–Ω–Ω–æ–≥–æ –∫–æ–¥–∞ —Å–æ–≥–ª–∞—Å–Ω–æ —É—Å–ª–æ–≤–∏—é –∑–∞–¥–∞—á–∏ –∏ –≤—ã–≤–µ—Å—Ç–∏
# –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ü—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ –≤—Å–µ–≥–æ Python –∫–æ–¥–∞, –∞ —Ç–∞–∫–∂–µ –ø—Ä–æ–±–µ–ª—ã –≤ –∫–æ–Ω—Ü–µ —Å—Ç—Ä–æ–∫
# –∫–æ–¥–∞ –ø—Ä–∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏–∏ –æ—Ç–≤–µ—Ç–æ–≤ –Ω–µ —É—á–∏—Ç—ã–≤–∞—é—Ç—Å—è.

from re import sub, DOTALL, MULTILINE
import sys


del_comments_in_start_line = sub(r'^ *#+ .+?\n', r'', sys.stdin.read(), flags=MULTILINE)  # —Ä–µ–≥—É–ª—è—Ä–∫–∞ –¥–ª—è –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤ –≤ –Ω–∞—á–∞–ª–µ —Å—Ç—Ä–æ–∫–∏
del_multiline_comments = sub(r' *""".+?"""\n', r'', del_comments_in_start_line, flags=DOTALL)  # —Ä–µ–≥—É–ª—è—Ä–∫–∞ –¥–ª—è –º–Ω–æ–≥–æ—Å—Ç—Ä–æ—á–Ω—ã—Ö –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤
del_comments_after_code = sub(r' {2}#+ .+?\n', r'\n', del_multiline_comments)  # —Ä–µ–≥—É–ª—è—Ä–∫–∞ –¥–ª—è –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤ –ø–æ—Å–ª–µ –∫–æ–¥–∞

print(del_comments_after_code)
