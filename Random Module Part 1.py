# Напишите программу, которая с помощью модуля random генерирует 7 различных случайных чисел
# для лотерейного билета. Программа должна вывести числа в порядке возрастания на одной строке
# через один символ пробела.

# Примечание. Убедитесь, что сгенерированные числа не содержат дубликатов.

from random import *

numbers = []

while len(numbers) != 7:
    num = randrange(1, 50)
    if num not in numbers:
        numbers.append(num)
    else:
        continue

print(*sorted(numbers))