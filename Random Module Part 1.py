# Напишите программу, которая с помощью модуля random генерирует случайный пароль. Программа
# принимает на вход длину пароля и выводит случайный пароль, содержащий только символы
# английского алфавита a..z, A..Z (в нижнем и верхнем регистре).

# Примечание 1. Символам A..Z английского языка соответствуют номера с 65 по 90 в таблице
# символов ASCII.

# Примечание 2. Символам a..z английского языка соответствуют номера с 97 по 122 в таблице
# символов ASCII.

# Примечание 3. Используйте функцию chr() для получения символа по его номеру в таблице
# символов ASCII.

from random import *

length = int(input())

def password_generation(l):
    password = []
    for i in range(l):
        if randint(0, 1) == 0:
            password.append(chr(randint(65, 90)))
        else:
            password.append(chr(randint(97, 122)))
    return print(''.join(password))

password_generation(length)



