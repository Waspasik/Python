from copy import*

n = int(input())
matrix_A = [[int(i) for i in input().split()] for _ in range(n)]
m = int(input())

matrix_X = deepcopy(matrix_A)
counter = 0

while counter != m - 1:
    matrix_B = [[0] * n for _ in range(n)]
    for i in range(n):
        for j in range(n):
            for l in range(n):
                matrix_B[i][j] += matrix_X[i][l] * matrix_A[l][j]
    matrix_A = matrix_B
    counter += 1

for row in matrix_A:
    print(*row)