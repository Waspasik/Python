import string

eng_alphabet = list(string.ascii_lowercase)
symbols_dict = {}
result = ''
counter = 0

for char in input():
    symbols_dict[counter] = symbols_dict.get(counter, char)
    counter += 1

for char in input().lower():
    if char.isalpha():
        result += symbols_dict[eng_alphabet.index(char)]
    else:
        result += char
        
print(result)

