# left –¥–æ right –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ, –∞ –∑–∞—Ç–µ–º –≤–æ–∑–±—É–∂–¥–∞—é—â–∏–π –∏—Å–∫–ª—é—á–µ–Ω–∏–µ StopIteration.

# —Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ left <= right.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ü—Ä–æ—Å—Ç–æ–µ —á–∏—Å–ª–æ ‚Äî –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ, –∏–º–µ—é—â–µ–µ —Ä–æ–≤–Ω–æ –¥–≤–∞ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö
# –¥–µ–ª–∏—Ç–µ–ª—è ‚Äî –µ–¥–∏–Ω–∏—Ü—É –∏ —Å–∞–º–æ–≥–æ —Å–µ–±—è. –ï–¥–∏–Ω–∏—Ü–∞ –ø—Ä–æ—Å—Ç—ã–º —á–∏—Å–ª–æ–º –Ω–µ —è–≤–ª—è–µ—Ç—Å—è. 

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. –í —Ç–µ—Å—Ç–∏—Ä—É—é—â—É—é —Å–∏—Å—Ç–µ–º—É —Å–¥–∞–π—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, —Å–æ–¥–µ—Ä–∂–∞—â—É—é —Ç–æ–ª—å–∫–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º—É—é
# –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é primes(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def primes(left, right):
    count = 0
    divider = 2
    while left != right+1:
        if left != 1:
            while divider < left:
                if left % divider == 0:
                    count += 1
                divider += 1
            if count == 0:
                divider = 2
                yield left
                left += 1
            else:
                divider = 2
                count = 0
                left += 1
        else:
            left += 1



