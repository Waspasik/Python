# –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é card_deck(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def card_deck(suit):
    suits = ["–ø–∏–∫", "—Ç—Ä–µ—Ñ", "–±—É–±–µ–Ω", "—á–µ—Ä–≤–µ–π"]
    card_values = ("2", "3", "4", "5", "6", "7", "8", "9", "10", "–≤–∞–ª–µ—Ç", "–¥–∞–º–∞", "–∫–æ—Ä–æ–ª—å", "—Ç—É–∑")
    suits.remove(suit)
    while True:
        for st in suits:
            for value in card_values:
                yield f'{value} {st}'



