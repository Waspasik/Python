# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –í —Ç–µ—Å—Ç–∏—Ä—É—é—â—É—é —Å–∏—Å—Ç–µ–º—É —Å–¥–∞–π—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, —Å–æ–¥–µ—Ä–∂–∞—â—É—é —Ç–æ–ª—å–∫–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º—É—é
# –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é simple_sequence(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def simple_sequence():
    value = 1
    while True:
        for _ in range(value):
            yield value
        value += 1



