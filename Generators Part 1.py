# nested_list ‚Äî —Å–ø–∏—Å–æ–∫, —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏ –∫–æ—Ç–æ—Ä–æ–≥–æ —è–≤–ª—è—é—Ç—Å—è —Ü–µ–ª—ã–µ —á–∏—Å–ª–∞ –∏–ª–∏ —Å–ø–∏—Å–∫–∏, —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏
# –∫–æ—Ç–æ—Ä—ã—Ö, –≤ —Å–≤–æ—é –æ—á–µ—Ä–µ–¥—å, —Ç–∞–∫–∂–µ —è–≤–ª—è—é—Ç—Å—è –ª–∏–±–æ —Ü–µ–ª—ã–µ —á–∏—Å–ª–∞, –ª–∏–±–æ —Å–ø–∏—Å–∫–∏; –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç—å –º–æ–∂–µ—Ç
# –±—ã—Ç—å –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–π

# –§—É–Ω–∫—Ü–∏—è –¥–æ–ª–∂–Ω–∞ –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä, –ø–æ—Ä–æ–∂–¥–∞—é—â–∏–π –≤—Å–µ —á–∏—Å–ª–∞, —Å–æ–¥–µ—Ä–∂–∞—â–∏–µ—Å—è –≤ nested_list,
# –≤–∫–ª—é—á–∞—è –≤—Å–µ —á–∏—Å–ª–∞ –∏–∑ –≤—Å–µ—Ö –≤–ª–æ–∂–µ–Ω–Ω—ã—Ö —Å–ø–∏—Å–∫–æ–≤, –∞ –∑–∞—Ç–µ–º –≤–æ–∑–±—É–∂–¥–∞–µ—Ç –∏—Å–∫–ª—é—á–µ–Ω–∏–µ StopIteration.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –í —Ç–µ—Å—Ç–∏—Ä—É—é—â—É—é —Å–∏—Å—Ç–µ–º—É —Å–¥–∞–π—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, —Å–æ–¥–µ—Ä–∂–∞—â—É—é —Ç–æ–ª—å–∫–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º—É—é
# –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é flatten(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def flatten(nested_list):
    for item in nested_list:
        if isinstance(item, list):
            yield from flatten(item)
        else:
            yield item