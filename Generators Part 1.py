# –§—É–Ω–∫—Ü–∏—è –¥–æ–ª–∂–Ω–∞ –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä, –ø–æ—Ä–æ–∂–¥–∞—é—â–∏–π –±–µ—Å–∫–æ–Ω–µ—á–Ω—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö
# —á–∏—Å–µ–ª-–ø–∞–ª–∏–Ω–¥—Ä–æ–º–æ–≤.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ß–∏—Å–ª–æ-–ø–∞–ª–∏–Ω–¥—Ä–æ–º ‚Äî —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ —á–∏—Ç–∞–µ—Ç—Å—è –æ–¥–∏–Ω–∞–∫–æ–≤–æ –∫–∞–∫ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª–µ–≤–æ,
# —Ç–∞–∫ –∏ —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –í —Ç–µ—Å—Ç–∏—Ä—É—é—â—É—é —Å–∏—Å—Ç–µ–º—É —Å–¥–∞–π—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, —Å–æ–¥–µ—Ä–∂–∞—â—É—é —Ç–æ–ª—å–∫–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º—É—é
# –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é palindromes(), –Ω–æ –Ω–µ –∫–æ–¥, –≤—ã–∑—ã–≤–∞—é—â–∏–π –µ–µ.

def palindromes():
    number = 1
    while True:
        if str(number) == str(number)[::-1]:
            yield number
            number += 1
        number += 1



