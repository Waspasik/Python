from datetime import timedelta, date


def dates(start, count=None):
    counter = 0
    while not count == counter:
        counter += 1
        yield start
        if start != date(9999, 12, 30):
            start += timedelta(hours=24)
        else:
            yield date(9999, 12, 31)
            break



